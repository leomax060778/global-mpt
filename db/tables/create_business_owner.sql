CREATE COLUMN TABLE "MKTG_PLANNING_TOOL"."BUSINESS_OWNER" ("BUSINESS_OWNER_ID" BIGINT CS_FIXED GENERATED BY DEFAULT AS IDENTITY NOT NULL ,
	 "DESCRIPTION" NVARCHAR(255) NOT NULL ,
	 "CREATED_DATE_TZ" LONGDATE CS_LONGDATE DEFAULT CURRENT_TIMESTAMP,
	 "MODIFIED_DATE_TZ" LONGDATE CS_LONGDATE,
	 "CREATED_USER_ID" BIGINT CS_FIXED NOT NULL ,
	 "MODIFIED_USER_ID" BIGINT CS_FIXED,
	 "ENABLED" TINYINT CS_INT DEFAULT 1,
	 "DELETED" TINYINT CS_INT DEFAULT 0,
	 "CRM_KEY" NVARCHAR(15),
	 "DELETED_TOKEN" NVARCHAR(255),
	 PRIMARY KEY ("BUSINESS_OWNER_ID")) UNLOAD PRIORITY 5 AUTO MERGE
;
CREATE UNIQUE INDEX "UK_BUSINESS_OWNER_CRM_KEY" ON "MKTG_PLANNING_TOOL"."BUSINESS_OWNER" ( "CRM_KEY" ASC,
	 "DELETED_TOKEN" ASC )