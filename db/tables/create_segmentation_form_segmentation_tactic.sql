CREATE COLUMN TABLE "MKTG_PLANNING_TOOL"."SEGMENTATION_FORM_SEGMENTATION_TACTIC" (
    SEGMENTATION_FORM_SEGMENTATION_TACTIC_ID BIGINT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    SEGMENTATION_FORM_ID BIGINT NOT NULL,
    SEGMENTATION_TACTIC_ID BIGINT NOT NULL,
    CREATED_DATE_TZ TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    MODIFIED_DATE_TZ TIMESTAMP,
    CREATED_USER_ID BIGINT NOT NULL,
    MODIFIED_USER_ID BIGINT,
    ENABLED TINYINT DEFAULT 1,
    DELETED TINYINT DEFAULT 0,
    FOREIGN KEY ("SEGMENTATION_FORM_ID") REFERENCES "MKTG_PLANNING_TOOL"."SEGMENTATION_FORM"("SEGMENTATION_FORM_ID"),
    FOREIGN KEY ("SEGMENTATION_TACTIC_ID") REFERENCES "MKTG_PLANNING_TOOL"."SEGMENTATION_TACTIC"("SEGMENTATION_TACTIC_ID"),
    FOREIGN KEY ("CREATED_USER_ID") REFERENCES "MKTG_PLANNING_TOOL"."USER"("USER_ID")
);