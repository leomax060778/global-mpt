CREATE COLUMN TABLE "MKTG_PLANNING_TOOL"."RESOURCE" ("RESOURCE_ID" BIGINT CS_FIXED GENERATED BY DEFAULT AS IDENTITY NOT NULL ,
	 "NAME" NVARCHAR(255),
	 "CREATED_DATE_TZ" LONGDATE CS_LONGDATE DEFAULT CURRENT_TIMESTAMP,
	 "MODIFIED_DATE_TZ" LONGDATE CS_LONGDATE,
	 "CREATED_USER_ID" BIGINT CS_FIXED NOT NULL ,
	 "MODIFIED_USER_ID" BIGINT CS_FIXED,
	 "ENABLED" TINYINT CS_INT DEFAULT 1,
	 "DELETED" TINYINT CS_INT DEFAULT 0,
	 "OBJECT_NAME" NVARCHAR(255) NOT NULL ,
	 PRIMARY KEY ("RESOURCE_ID")) UNLOAD PRIORITY 5 AUTO MERGE
;
CREATE INDEX "IDX_RESOURCE_OBJECT_NAME" ON "MKTG_PLANNING_TOOL"."RESOURCE" ( "OBJECT_NAME" ASC )