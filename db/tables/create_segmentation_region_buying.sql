create COLUMN TABLE "MKTG_PLANNING_TOOL"."SEGMENTATION_REGION_BUYING" (
    SEGMENTATION_REGION_BUYING_ID BIGINT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    NAME NVARCHAR(255) NOT NULL,
    CREATED_DATE_TZ TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    MODIFIED_DATE_TZ TIMESTAMP,
    CREATED_USER_ID BIGINT NOT NULL,
    MODIFIED_USER_ID BIGINT,
    ENABLED TINYINT DEFAULT 1,
    DELETED TINYINT DEFAULT 0,
    deleted_token nvarchar(255) null,
    FOREIGN KEY ("CREATED_USER_ID") REFERENCES "MKTG_PLANNING_TOOL"."USER"("USER_ID")
);
