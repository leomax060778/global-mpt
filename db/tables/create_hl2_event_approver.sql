CREATE COLUMN TABLE HL2_EVENT_APPROVER (
    HL2_EVENT_APPROVER_ID BIGINT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    HL2_ID BIGINT NOT NULL,
    USER_ID BIGINT NOT NULL,
    CREATED_DATE_TZ TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	MODIFIED_DATE_TZ TIMESTAMP,
	CREATED_USER_ID BIGINT NOT NULL,
	MODIFIED_USER_ID BIGINT,
	ENABLED TINYINT DEFAULT 1,
	DELETED TINYINT DEFAULT 0,
    FOREIGN KEY (HL2_ID) REFERENCES "MKTG_PLANNING_TOOL".HL2(HL2_ID),
    FOREIGN KEY (USER_ID) REFERENCES "MKTG_PLANNING_TOOL".USER(USER_ID),
    FOREIGN KEY (CREATED_USER_ID) REFERENCES "MKTG_PLANNING_TOOL".USER(USER_ID)
);